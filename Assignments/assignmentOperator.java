package assingmentOperators;

public class assignmentOperator {

	public static void main(String[] args) {
		int a = 32;
		System.out.println("Before assingment operator "+a);
		a+=a;//a=a+a
		System.out.println("After +=assingment operator "+a);
		a-=a;//a=a-a;
		System.out.println("After -=assingment operator "+a);
		a*=a;//a=a*a;
		System.out.println("After *=assingment operator "+a);
		a=43;
		a/=a;//a=a/a;
		System.out.println("After /=assingment operator "+a);
		a%=a;//a=a%a;
		System.out.println("After +=assingment operator "+a);

	}

}
